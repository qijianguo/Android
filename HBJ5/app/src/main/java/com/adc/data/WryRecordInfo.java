package com.adc.data;

public class WryRecordInfo {
	private String record_type;
	private String record_passed;
	private String record_time;
	private String record_content;
	private String pollution_record_id;
	private String pollution_source_id;
	public String getRecord_type() {
		return record_type;
	}
	public void setRecord_type(String record_type) {
		this.record_type = record_type;
	}
	public String getRecord_passed() {
		return record_passed;
	}
	public void setRecord_passed(String record_passed) {
		this.record_passed = record_passed;
	}
	public String getRecord_time() {
		return record_time;
	}
	public void setRecord_time(String record_time) {
		this.record_time = record_time;
	}
	public String getRecord_content() {
		return record_content;
	}
	public void setRecord_content(String record_content) {
		this.record_content = record_content;
	}
	public String getPollution_record_id() {
		return pollution_record_id;
	}
	public void setPollution_record_id(String pollution_record_id) {
		this.pollution_record_id = pollution_record_id;
	}
	public String getPollution_source_id() {
		return pollution_source_id;
	}
	public void setPollution_source_id(String pollution_source_id) {
		this.pollution_source_id = pollution_source_id;
	}

}
